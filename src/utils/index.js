/**
 * @returns {string}
 */
export function createUniqueString() {
  const timestamp = +new Date() + ''
  const randomNum = parseInt((1 + Math.random()) * 65536) + ''
  return (+(randomNum + timestamp)).toString(32)
}

/**
 * Check if an element has a class
 * @param {HTMLElement} elm
 * @param {string} cls
 * @returns {boolean}
 */
export function hasClass(ele, cls) {
  return !!ele.className.match(new RegExp('(\\s|^)' + cls + '(\\s|$)'))
}

/**
 * Add class to element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function addClass(ele, cls) {
  if (!hasClass(ele, cls)) ele.className += ' ' + cls
}

/**
 * Remove class from element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function removeClass(ele, cls) {
  if (hasClass(ele, cls)) {
    const reg = new RegExp('(\\s|^)' + cls + '(\\s|$)')
    ele.className = ele.className.replace(reg, ' ')
  }
}

/**
 * 保留自定义小数位，不四舍五入
 * @param num
 * @param n
 * @returns {string|boolean}
 */
export function toFixedDigit(num, n) { // 保留n位
  if (typeof num !== 'number') {
    return false
  }

  num = num.toString()
  var result = ''
  var zeroResult = function(n) {
    var zero = ''
    for (var i = 0; i < n; i++) {
      zero += 0
    }
    return zero
  }
  if (num % 1 === 0) { // 整数
    result = num + '.' + zeroResult(n)
  } else { // 小数
    var num1 = num.split('.')
    if (num1[1].length < n) {
      result = num1[0] + '.' + num1[1] + zeroResult(n - num1[1].length)
    } else {
      result = num1[0] + '.' + num1[1].substring(0, n)
    }
  }
  return result
}

/**
 * 树形数据转换
 * @param {*} data
 * @param {*} id
 * @param {*} pid
 */
export function listTree(data, id = 'id', pid = 'pid') {
  const res = []
  const temp = {}

  for (let i = 0; i < data.length; i++) {
    temp[data[i][id]] = data[i]
  }

  for (let k = 0; k < data.length; k++) {
    if (temp[data[k][pid]] && data[k][id] !== data[k][pid]) {
      if (!temp[data[k][pid]]['children']) {
        temp[data[k][pid]]['children'] = []
      }

      if (!temp[data[k][pid]]['_level']) {
        temp[data[k][pid]]['_level'] = 1
      }

      data[k]['parent'] = temp[data[k][pid]]
      data[k]['_level'] = temp[data[k][pid]]._level + 1

      temp[data[k][pid]]['children'].push(data[k])
    } else {
      res.push(data[k])
    }
  }
  return res
}

